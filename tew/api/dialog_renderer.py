def _set_color(renderer: object, color: tuple[int, int, int], alpha: int = 255) -> None:
    SDL_SetRenderDrawColor(renderer, color[0], color[1], color[2], alpha)


def _fill_rect(renderer: object, x: int, y: int, w: int, h: int) -> None:
    if w <= 0 or h <= 0:
        return
    r = SDL_Rect(x, y, w, h)
    SDL_RenderFillRect(renderer, r)


def _draw_rect(renderer: object, x: int, y: int, w: int, h: int) -> None:
    if w <= 0 or h <= 0:
        return
    r = SDL_Rect(x, y, w, h)
    SDL_RenderDrawRect(renderer, r)


def render_text(
    renderer: object,
    text: str,
    x: int,
    y: int,
    color: tuple[int, int, int],
) -> None:
    """Render a string using the embedded 8x8 bitmap font.

    Each character occupies an 8x8 pixel cell.  Characters outside the
    printable ASCII range (0x20–0x7E) are rendered as spaces.
    """
    _set_color(renderer, color)
    cx = x
    for ch in text:
        code = ord(ch)
        if code < 0x20 or code > 0xFF:
            code = 0x20   # render unsupported as space
        glyph = _glyph(code)
        for row, byte in enumerate(glyph):
            for bit in range(8):
                if byte & (0x80 >> bit):
                    # Draw a single pixel as a 1x1 filled rect
                    r = SDL_Rect(cx + bit, y + row, 1, 1)
                    SDL_RenderFillRect(renderer, r)
        cx += 8


def _draw_3d_border(
    renderer: object,
    x: int, y: int, w: int, h: int,
    *,
    raised: bool = True,
) -> None:
    """Draw a simple 2-pixel Win32-style raised or sunken border."""
    if raised:
        hilite = COLOR_BUTTON_HILITE
        shadow = COLOR_BUTTON_SHADOW
        dark   = COLOR_BUTTON_BORDER
    else:
        hilite = COLOR_BUTTON_SHADOW
        shadow = COLOR_BUTTON_HILITE
        dark   = COLOR_BUTTON_BORDER

    # Outer highlight (top-left)
    _set_color(renderer, hilite)
    SDL_RenderDrawLine(renderer, x, y, x + w - 2, y)
    SDL_RenderDrawLine(renderer, x, y, x, y + h - 2)
    # Inner highlight
    _set_color(renderer, hilite)
    SDL_RenderDrawLine(renderer, x + 1, y + 1, x + w - 3, y + 1)
    SDL_RenderDrawLine(renderer, x + 1, y + 1, x + 1, y + h - 3)
    # Outer shadow (bottom-right)
    _set_color(renderer, dark)
    SDL_RenderDrawLine(renderer, x + w - 1, y, x + w - 1, y + h - 1)
    SDL_RenderDrawLine(renderer, x, y + h - 1, x + w - 1, y + h - 1)
    # Inner shadow
    _set_color(renderer, shadow)
    SDL_RenderDrawLine(renderer, x + w - 2, y + 1, x + w - 2, y + h - 2)
    SDL_RenderDrawLine(renderer, x + 1, y + h - 2, x + w - 2, y + h - 2)


# ── Control renderers ─────────────────────────────────────────────────────────

def _render_button(
    renderer: object,
    child: WindowEntry,
    px: int, py: int, pw: int, ph: int,
) -> None:
    """Render a BUTTON control."""
    bs_type = child.style & 0x0F
    is_checkbox = bs_type in (0x02, 0x03)  # BS_CHECKBOX, BS_AUTOCHECKBOX

    if is_checkbox:
        # Draw checkbox box (13x13) then label
        box_size = 13
        box_x = px + 2
        box_y = py + (ph - box_size) // 2

        _set_color(renderer, COLOR_EDIT_BG)
        _fill_rect(renderer, box_x, box_y, box_size, box_size)
        _set_color(renderer, COLOR_BUTTON_BORDER)
        _draw_rect(renderer, box_x, box_y, box_size, box_size)

        if child.check_state:
            # Draw a checkmark (two lines)
            _set_color(renderer, COLOR_CHECK_MARK)
            SDL_RenderDrawLine(renderer, box_x + 2, box_y + 6, box_x + 5, box_y + 9)
            SDL_RenderDrawLine(renderer, box_x + 5, box_y + 9, box_x + 10, box_y + 3)

        label_x = box_x + box_size + 4
        label_y = py + (ph - 8) // 2
        render_text(renderer, child.title, label_x, label_y, COLOR_TEXT)

    else:
        # Standard push button with 3D border
        _set_color(renderer, COLOR_BUTTON_BG)
        _fill_rect(renderer, px, py, pw, ph)
        _draw_3d_border(renderer, px, py, pw, ph, raised=True)

        # Centered label
        text_w = len(child.title) * 8
        label_x = px + max(0, (pw - text_w) // 2)
        label_y = py + max(0, (ph - 8) // 2)
        render_text(renderer, child.title, label_x, label_y, COLOR_TEXT)


def _render_edit(
    renderer: object,
    child: WindowEntry,
    px: int, py: int, pw: int, ph: int,
    focused: bool,
) -> None:
    """Render an EDIT control."""
    is_password = bool(child.style & 0x20)

    _set_color(renderer, COLOR_EDIT_BG)
    _fill_rect(renderer, px, py, pw, ph)
    _set_color(renderer, COLOR_EDIT_BORDER)
    _draw_rect(renderer, px, py, pw, ph)

    if focused:
        _set_color(renderer, COLOR_FOCUS_BORDER)
        _draw_rect(renderer, px - 1, py - 1, pw + 2, ph + 2)

    display = ("*" * len(child.title)) if is_password else child.title
    # Clip display text to visible area
    visible_chars = max(0, (pw - 4) // 8)
    if len(display) > visible_chars:
        display = display[-visible_chars:]

    render_text(renderer, display, px + 2, py + (ph - 8) // 2, COLOR_TEXT)

    # Cursor blink is not tracked here — always draw a cursor when focused
    if focused:
        cursor_x = px + 2 + len(display) * 8
        cursor_y_top = py + 2
        cursor_y_bot = py + ph - 3
        _set_color(renderer, COLOR_TEXT)
        SDL_RenderDrawLine(renderer, cursor_x, cursor_y_top, cursor_x, cursor_y_bot)


def _render_static(
    renderer: object,
    child: WindowEntry,
    px: int, py: int, pw: int, ph: int,
) -> None:
    """Render a STATIC control (label text or bitmap image).

    A title starting with ``"#"`` indicates a bitmap resource reference
    (parsed from the Win32 dialog template's 0xFFFF ordinal encoding).
    When a preloaded texture is available it is stretched to fill the
    control rectangle; otherwise a plain grey placeholder is drawn.
    """
    title = child.title

    if title.startswith("#"):
        if child.bitmap_texture is not None:
            # Render the actual bitmap resource, stretched to the control rect.
            dst = SDL_Rect(px, py, pw, ph)
            SDL_RenderCopy(renderer, child.bitmap_texture, None, dst)
        else:
            # Bitmap resource not available — draw a plain grey rectangle.
            _set_color(renderer, (180, 180, 180))
            _fill_rect(renderer, px, py, pw, ph)
            _set_color(renderer, COLOR_BUTTON_BORDER)
            _draw_rect(renderer, px, py, pw, ph)
    else:
        render_text(renderer, title, px, py + (ph - 8) // 2, COLOR_TEXT)


# ── Main dialog render entry point ────────────────────────────────────────────

def render_dialog(wm: WindowManager, dlg_hwnd: int) -> None:
    """Render the dialog and all its child controls to the SDL2 window.

    Clears the renderer, draws the dialog background and all controls,
    then calls SDL_RenderPresent.  Does nothing if the HWND is unknown or
    has no SDL renderer attached.
    """
    entry = wm.get_window(dlg_hwnd)
    if entry is None:
        logger.warn("dialog", f"[DialogRenderer] render_dialog: unknown hwnd=0x{dlg_hwnd:x}")
        return

    renderer = entry.sdl_renderer
    if renderer is None:
        logger.warn("dialog", f"[DialogRenderer] render_dialog: no renderer for hwnd=0x{dlg_hwnd:x}")
        return

    # ── Background ────────────────────────────────────────────────────────────
    _set_color(renderer, COLOR_DIALOG_BG)
    SDL_RenderClear(renderer)

    dlg_w = du_to_px_x(entry.cx)
    dlg_h = du_to_px_y(entry.cy)

    # ── Title bar ─────────────────────────────────────────────────────────────
    _set_color(renderer, COLOR_TITLE_BG)
    _fill_rect(renderer, 0, 0, dlg_w, TITLE_BAR_H)
    render_text(renderer, entry.title, 4, (TITLE_BAR_H - 8) // 2, COLOR_TITLE_TEXT)

    # ── Child controls ────────────────────────────────────────────────────────
    for ctrl_id, child_hwnd in entry.children.items():
        child = wm.get_window(child_hwnd)
        if child is None:
            continue

        px = du_to_px_x(child.x)
        py = du_to_px_y(child.y) + TITLE_BAR_H
        pw = du_to_px_x(child.cx)
        ph = du_to_px_y(child.cy)

        class_lower = child.class_name.lower()
        focused = (child_hwnd == wm._focused_hwnd)

        if class_lower == "button":
            _render_button(renderer, child, px, py, pw, ph)
        elif class_lower == "edit":
            _render_edit(renderer, child, px, py, pw, ph, focused)
        elif class_lower == "static":
            _render_static(renderer, child, px, py, pw, ph)
        else:
            # Unknown class — draw a placeholder box
            _set_color(renderer, (200, 200, 220))
            _fill_rect(renderer, px, py, pw, ph)
            _set_color(renderer, COLOR_BUTTON_BORDER)
            _draw_rect(renderer, px, py, pw, ph)
            render_text(renderer, child.class_name[:6], px + 1, py + 1, COLOR_TEXT)

    SDL_RenderPresent(renderer)
