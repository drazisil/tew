def _glyph(char_code: int) -> bytes:
    """Return 8 bytes of glyph data for the given character code (0x00–0xFF)."""
    idx = max(0, min(char_code, 255))
    return _FONT_8X8_RAW[idx * 8 : idx * 8 + 8]


